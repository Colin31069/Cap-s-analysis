import { spawn } from "node:child_process";
import { mkdir, readdir, writeFile } from "node:fs/promises";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const projectRoot = resolve(dirname(fileURLToPath(import.meta.url)), "..");
const tauriRoot = resolve(projectRoot, "src-tauri");
const vendorDir = resolve(tauriRoot, "vendor");
const cargoConfigDir = resolve(tauriRoot, ".cargo");
const cargoConfigPath = resolve(cargoConfigDir, "config.toml");
const cargoCommand = process.platform === "win32" ? "cargo.exe" : "cargo";
const refreshVendor = process.argv.includes("--refresh");

function run(command, args, cwd) {
  return new Promise((resolveRun, reject) => {
    const child = spawn(command, args, { cwd, stdio: "inherit", shell: false });
    child.on("error", reject);
    child.on("exit", (code) => {
      if (code === 0) {
        resolveRun();
      } else {
        reject(new Error(`${command} ${args.join(" ")} exited with code ${code}`));
      }
    });
  });
}

async function hasCompleteVendorDir() {
  try {
    const entries = await readdir(vendorDir, { withFileTypes: true });
    return (
      entries.some((entry) => entry.isDirectory() && !entry.name.startsWith(".vendor-staging")) &&
      !entries.some((entry) => entry.name.startsWith(".vendor-staging"))
    );
  } catch {
    return false;
  }
}

if (refreshVendor || !(await hasCompleteVendorDir())) {
  console.log("[vendor] Copying Cargo crates into src-tauri/vendor...");
  await run(cargoCommand, ["vendor", "vendor", "--locked"], tauriRoot);
} else {
  console.log("[vendor] Existing src-tauri/vendor directory found; use `npm run deps:vendor -- --refresh` to rebuild it.");
}

await mkdir(cargoConfigDir, { recursive: true });
await writeFile(
  cargoConfigPath,
  [
    "# Generated by `npm run deps:vendor`.",
    "# Delete this file to use the normal Cargo registry cache again.",
    "",
    "[source.crates-io]",
    'replace-with = "vendored-sources"',
    "",
    "[source.vendored-sources]",
    'directory = "vendor"',
    "",
  ].join("\n"),
);

console.log("[vendor] Wrote src-tauri/.cargo/config.toml.");
console.log("[vendor] Cargo builds will now use src-tauri/vendor for crate sources.");
